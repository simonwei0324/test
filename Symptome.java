public class Symptome {
	private String libelle;
	
	//Constructeur
	/**
	 * @param libelle
	 */
	public Symptome(String libelle) {
		super();
		this.libelle = libelle;
	}


	
	//Getters and Setters

	/**
	 * @return the libelle
	 */
	public String getLibelle() {
		return libelle;
	}



	/**
	 * @param libelle the libelle to set
	 */
	public void setLibelle(String libelle) {
		this.libelle = libelle;
	}
	
	

}
